public class passenger {
    private String passenger_FirstName;
    private String Passenger_Surname;
    private double expenses;


    public String getPassenger_FirstName() {
        return passenger_FirstName;
    }

    public void setPassenger_FirstName(String passenger_FirstName) {
        this.passenger_FirstName = passenger_FirstName;
    }

    public String getPassenger_Surname() {
        return Passenger_Surname;
    }

    public void setPassenger_Surname(String Passenger_Surname) {
        this.Passenger_Surname = Passenger_Surname;
    }

    public double getExpenses() {
        return expenses;
    }

    public void setExpenses(double expenses) {
        this.expenses = expenses;
    }

}